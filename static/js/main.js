const checkout = document.getElementById('checkout');
if(checkin && checkout) {
    const today = new Date().toISOString().split('T')[0];
    checkin.min = today;
    
    window.updateCheckoutMin = function() {
        if(checkin.value) {
            const checkinDate = new Date(checkin.value);
            checkinDate.setDate(checkinDate.getDate() + 1);
            checkout.min = checkinDate.toISOString().split('T')[0];
            if(checkout.value && new Date(checkout.value) <= new Date(checkin.value)) {
                checkout.value = checkout.min;
            }
        }
    };
}

// Mobile Menu
const btn = document.getElementById('mobile-menu-btn');
const menu = document.getElementById('mobile-menu');
if(btn && menu) {
    btn.addEventListener('click', () => {
        menu.classList.toggle('open');
    });
    document.querySelectorAll('#mobile-menu a').forEach(link => {
        link.addEventListener('click', () => {
            menu.classList.remove('open');
        });
    });
}

// Navbar Scroll
window.addEventListener('scroll', () => {
    const nav = document.getElementById('navbar');
    if(nav) {
        if (window.scrollY > 50) {
            nav.classList.remove('nav-transparent', 'py-4');
            nav.classList.add('nav-solid', 'py-2');
        } else {
            nav.classList.add('nav-transparent', 'py-4');
            nav.classList.remove('nav-solid', 'py-2');
        }
    }
});

// Intersection Observer (Animations + Stats)
const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('visible');
            
            // Stats animation
            if(entry.target.classList.contains('stat-number')) {
                const target = parseInt(entry.target.getAttribute('data-target'));
                let count = 0;
                const duration = 2000;
                const increment = target / (duration / 16);
                
                const updateCount = () => {
                    count += increment;
                    if (count < target) {
                        entry.target.innerText = Math.ceil(count);
                        requestAnimationFrame(updateCount);
                    } else {
                        entry.target.innerText = target;
                    }
                };
                updateCount();
                entry.target.classList.remove('stat-number'); // run once
            }
        }
    });
}, { threshold: 0.1 });

document.querySelectorAll('.fade-in-up, .stat-number').forEach(el => {
    observer.observe(el);
});

